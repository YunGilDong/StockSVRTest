//------------------------------------------------------------------------------
// Delete
//------------------------------------------------------------------------------
void CLSstockCL::Delete(void)
{
	if (TcpIF != NULL)
	{
		TcpIF->Close();
		delete TcpIF;
		TcpIF = NULL;
	}
	if (Thread != NULL)
	{
		if (Thread->Active)
			Thread->Stop(true);
		delete Thread;
		Thread = NULL;
	}
	Active = false;
}
//------------------------------------------------------------------------------
// Init
//------------------------------------------------------------------------------
void CLSstockCL::Init(void)
{
	ID = -1;
	Socket = -1;
	TcpIF = NULL;
	Thread = NULL;
	Active = false;
}
//------------------------------------------------------------------------------
void CLSstockCL::Init(DB_STOCKCL *pInfo)
{
	ID = pInfo->id;
	Mng.id = pInfo->id;
	sprintf(Mng.address, "%s", pInfo->address);
	SetName("STOCKCL%d", ID);
	Socket = -1;
	TcpIF = NULL;
	Thread = NULL;
	InitState(pInfo);
	Active = false;
}
